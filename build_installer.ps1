function WebView2Installed(): Boolean;
var v: string;
begin
  Result := RegQueryStringValue(HKLM, 'SOFTWARE\WOW6432Node\Microsoft\EdgeUpdate\Clients\{F3017226-FE2A-4295-8BDB-FCBA1C7C9C2B}', 'pv', v) or
            RegQueryStringValue(HKLM, 'SOFTWARE\Microsoft\EdgeUpdate\Clients\{F3017226-FE2A-4295-8BDB-FCBA1C7C9C2B}', 'pv', v);
end;
