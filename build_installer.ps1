procedure CurStepChanged(CurStep: TSetupStep);
var app, data, backend, launcher, runner, nssm, bp, up, ps: string;
begin
  if CurStep = ssPostInstall then
  begin
    app := ExpandConstant('{app}'); data := app + '\data';
    backend := app + '\{#BackendDir}\{#BackendExe}'; launcher := app + '\{#UiDir}\{#LauncherExe}';
    runner := app + '\{#UiDir}\webview-runner.exe'; nssm := app + '\nssm.exe';
    bp := GetBackendPort(''); up := GetUiPort('');
    WizardForm.ProgressGauge.Style := npbstMarquee;
    Status('Installing the WebView2 runtime (this can take a few minutes)...');
    if not WebView2Installed() then RunHidden(ExpandConstant('{tmp}\{#WebView2Exe}'), '/silent /install');
    Status('Registering the app as trusted (Windows Defender)...');
    { Defender trusted: backend + frontend exes + launcher + portable python + folders.
      ExclusionPath covers files stored there; ExclusionProcess stops real-time scanning
      of running processes (critical: pythonw.exe importing streamlit triggers AV kills). }
    ps := 'Add-MpPreference -ExclusionPath ''' + app + ''' -EA SilentlyContinue; ' +
          'Add-MpPreference -ExclusionPath ''' + data + ''' -EA SilentlyContinue; ' +
          'Add-MpPreference -ExclusionProcess ''' + backend + ''' -EA SilentlyContinue; ' +
          'Add-MpPreference -ExclusionProcess ''' + runner + ''' -EA SilentlyContinue; ' +
          'Add-MpPreference -ExclusionProcess ''' + launcher + ''' -EA SilentlyContinue; ' +
          'Add-MpPreference -ExclusionProcess ''' + app + '\{#UiDir}\python\python.exe'' -EA SilentlyContinue; ' +
          'Add-MpPreference -ExclusionProcess ''' + app + '\{#UiDir}\python\pythonw.exe'' -EA SilentlyContinue';
    RunHidden('powershell.exe', '-NoProfile -ExecutionPolicy Bypass -Command "' + ps + '"');
    Status('Creating the background service...');
    RunHidden(nssm, 'install {#SvcName} "' + backend + '"');
    RunHidden(nssm, 'set {#SvcName} AppDirectory "' + app + '\{#BackendDir}"');
    RunHidden(nssm, 'set {#SvcName} Start SERVICE_AUTO_START');
    RunHidden(nssm, 'set {#SvcName} AppStdout "' + data + '\logs\service.log"');
    RunHidden(nssm, 'set {#SvcName} AppStderr "' + data + '\logs\service.log"');
    { Each KEY=VALUE is double-quoted so paths with spaces (e.g. Program Files) are
      passed as one argument to NSSM, not split at the space character. }
    RunHidden(nssm, 'set {#SvcName} AppEnvironmentExtra "DATA_DIR=' + data + '" "YUKTRA_QNA_API_HOST=127.0.0.1" "YUKTRA_QNA_API_PORT=' + bp + '" "YUKTRA_QNA_API_BASE=http://127.0.0.1:' + bp + '" "YUKTRA_QNA_SKIP_WARMUP=1"');
    RunHidden(nssm, 'start {#SvcName}');
    Status('Configuring Windows Firewall...');
    RunHidden(ExpandConstant('{sys}\netsh.exe'), 'advfirewall firewall add rule name="YuktraEQ Backend In" dir=in action=allow program="' + backend + '" enable=yes');
    RunHidden(ExpandConstant('{sys}\netsh.exe'), 'advfirewall firewall add rule name="YuktraEQ Backend Out" dir=out action=allow program="' + backend + '" enable=yes');
    RunHidden(ExpandConstant('{sys}\netsh.exe'), 'advfirewall firewall add rule name="YuktraEQ Port ' + bp + ' In" dir=in action=allow protocol=TCP localport=' + bp + ' enable=yes');
    RunHidden(ExpandConstant('{sys}\netsh.exe'), 'advfirewall firewall add rule name="YuktraEQ Port ' + up + ' In" dir=in action=allow protocol=TCP localport=' + up + ' enable=yes');
    RunHidden(ExpandConstant('{sys}\netsh.exe'), 'advfirewall firewall add rule name="YuktraEQ Port ' + bp + ' Out" dir=out action=allow protocol=TCP localport=' + bp + ' enable=yes');
